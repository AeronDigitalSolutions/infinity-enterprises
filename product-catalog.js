const catalog = document.querySelector('#product-catalog');
const resultCount = document.querySelector('#product-count');
const searchInput = document.querySelector('#product-search');
const viewButtons = document.querySelectorAll('[data-view]');
const catalogShell = document.querySelector('.catalog-shell');
const categoryTabs = document.querySelector('#category-tabs');
const categorySelect = document.querySelector('#category-select');

let currentCategory = 'all';
const categories = [...new Set(PRODUCTS.map(p => p.category))].sort();

function productInitial(name) {
  return name.replace(/^SHELL\s+/i, '').charAt(0) || 'P';
}

function renderCategories() {
  if (categoryTabs) {
    categoryTabs.innerHTML = `<button class="category-tab active" data-category="all">All</button>` + 
      categories.map(cat => `<button class="category-tab" data-category="${cat}">${cat}</button>`).join('');
  }
  if (categorySelect) {
    categorySelect.innerHTML = `<option value="all">All Categories</option>` + 
      categories.map(cat => `<option value="${cat}">${cat}</option>`).join('');
  }
}

function setCategory(category) {
  currentCategory = category;
  
  if (categoryTabs) {
    categoryTabs.querySelectorAll('.category-tab').forEach(tab => {
      tab.classList.toggle('active', tab.dataset.category === category);
    });
  }
  if (categorySelect && categorySelect.value !== category) {
    categorySelect.value = category;
  }
  
  renderProducts();
}

function renderProducts() {
  const query = searchInput.value.trim().toLowerCase();
  const filtered = PRODUCTS.filter(product => {
    const matchesSearch = product.name.toLowerCase().includes(query);
    const matchesCategory = currentCategory === 'all' || product.category === currentCategory;
    return matchesSearch && matchesCategory;
  });
  
  resultCount.textContent = `${filtered.length} product${filtered.length === 1 ? '' : 's'}`;
  catalog.innerHTML = filtered.map((product, index) => `
    <a class="catalog-item" href="${encodeURI(product.file)}" target="_blank" rel="noopener" style="--delay:${Math.min(index * 18, 320)}ms">
      <span class="product-badge">${productInitial(product.name)}</span>
      <span class="product-copy">
        <strong>${product.name}</strong>
        <small>${product.category} product data sheet</small>
      </span>
      <span class="open-pdf">View Details</span>
    </a>
  `).join('') || `<div class="empty-state">No products found. Try another search or category.</div>`;
}

searchInput.addEventListener('input', renderProducts);

if (categoryTabs) {
  categoryTabs.addEventListener('click', (e) => {
    if (e.target.matches('.category-tab')) {
      setCategory(e.target.dataset.category);
    }
  });
}

if (categorySelect) {
  categorySelect.addEventListener('change', (e) => {
    setCategory(e.target.value);
  });
}

viewButtons.forEach(button => {
  button.addEventListener('click', () => {
    viewButtons.forEach(item => item.classList.toggle('active', item === button));
    catalogShell.dataset.view = button.dataset.view;
  });
});

renderCategories();
renderProducts();
