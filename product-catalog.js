const catalog = document.querySelector('#product-catalog');
const resultCount = document.querySelector('#product-count');
const searchInput = document.querySelector('#product-search');
const viewButtons = document.querySelectorAll('[data-view]');
const catalogShell = document.querySelector('.catalog-shell');

function productInitial(name) {
  return name.replace(/^SHELL\s+/i, '').charAt(0) || 'P';
}

function renderProducts() {
  const query = searchInput.value.trim().toLowerCase();
  const filtered = PRODUCTS.filter(product => product.name.toLowerCase().includes(query));
  resultCount.textContent = `${filtered.length} product${filtered.length === 1 ? '' : 's'}`;
  catalog.innerHTML = filtered.map((product, index) => `
    <a class="catalog-item" href="${encodeURI(product.file)}" target="_blank" rel="noopener" style="--delay:${Math.min(index * 18, 320)}ms">
      <span class="product-badge">${productInitial(product.name)}</span>
      <span class="product-copy">
        <strong>${product.name}</strong>
        <small>${product.category} product data sheet</small>
      </span>
      <span class="open-pdf">Open PDF</span>
    </a>
  `).join('') || `<div class="empty-state">No products found. Try another product name.</div>`;
}

searchInput.addEventListener('input', renderProducts);

viewButtons.forEach(button => {
  button.addEventListener('click', () => {
    viewButtons.forEach(item => item.classList.toggle('active', item === button));
    catalogShell.dataset.view = button.dataset.view;
  });
});

renderProducts();
